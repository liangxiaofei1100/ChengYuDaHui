package com.zhaoyan.juyou.game.chengyu;

public class ChengYu {
	String name;
	String pinyin;
	String comment;
	String original;
	String example;

	public ChengYu(String name, String pinyin, String comment, String original,
			String example) {
		super();
		this.name = name;
		this.pinyin = pinyin;
		this.comment = comment;
		this.original = original;
		this.example = example;
	}

}